<script>

  import AppLayout from '@/Layouts/AppLayout'
 
  export default {
    components: 
    {
      AppLayout,
    },
    props: {
      cards: Array,
    },
    data:function(){
        return{
          toggle: false,
          remaining:this.cards.length,
          trueAnswer :0,
          wrongAnswer:0,
          card_text:'',
          i:0,
          flipped:true,
        } 
    },
    mounted: function() 
    {
      this.card_text= this.cards[0].front;
    },
    methods:{
        add: function(event)
        {
            if(this.i < this.cards.length-1)
            {
                this.trueAnswer++;
                this.i++;
                this.remaining--;
                this.card_text = this.cards[this.i].front;
            }
            else
            {
              this.trueAnswer++;
              this.remaining--;
              this.toggle = !this.toggle;
            }
            
        },
        subtract:function(event)
        {
            if(this.i < this.cards.length-1)
            {
                this.wrongAnswer++;
                this.i++;
                this.remaining--;
                this.card_text = this.cards[this.i].front
            }
            else
            {
              this.wrongAnswer++;
              this.remaining--;
              this.toggle = !this.toggle;
            }
        },
        toggleCard: function(flipped) 
        {
          if(this.flipped)
            this.card_text = this.cards[this.i].back
          else
            this.card_text = this.cards[this.i].front
          this.flipped = !flipped;
          
        }
    }
  }
</script>




